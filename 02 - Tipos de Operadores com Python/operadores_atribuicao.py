# Atribuuição Simples
saldo = 500
print(saldo)

saldo = 200
print(saldo)

# Atribuição com adição
saldo += 10
print(saldo)

# Atribuição com subtração
saldo -= 5
print(saldo)

# Atribuição com divisão
saldo /= 2
print(saldo)

# Atribuição com divisão inteira
saldo //= 2
print(saldo)

# Atribuição com multiplicação
saldo *= 10
print(saldo)

# Atribuição com módulo ou resto da divisão
saldo %= 4
print(saldo)

# Atribuição com exponenciação
saldo **= 2
print(saldo)
