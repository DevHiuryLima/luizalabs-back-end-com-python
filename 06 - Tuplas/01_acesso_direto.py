# Declarando uma tupla com elementos de frutas
# Os elementos estão organizados em posições (índices) começando do 0
frutas = ("maçã", "laranja", "uva", "pera",)
#          [0]      [1]       [2]    [3]

# Acessando um elemento por índice direto
# frutas[0] acessa o primeiro elemento da tupla (índice 0)
print(frutas[0])  # maçã


# Acessando outro elemento por índice
# frutas[2] acessa o terceiro elemento da tupla (índice 2)
print(frutas[2])  # uva
