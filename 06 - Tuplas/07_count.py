# Método count() em tuplas

# Cria uma tupla com alguns elementos repetidos
cores = ("vermelho", "azul", "verde", "azul",)


# count() retorna o número de ocorrências de um elemento específico na tupla.
print(cores.count("vermelho"))
# Saída: 1


print(cores.count("azul"))
# Saída: 2


print(cores.count("verde"))
# Saída: 1
