# Declarando listas em Python
# Listas são coleções ordenadas e mutáveis de itens
# Podem conter elementos de diferentes tipos de dados
# São definidas usando colchetes []


# Exemplo de declaração de listas:
frutas = ["laranja", "maca", "uva"]
print(frutas)


frutas = []
print(frutas)


letras = list("python")
print(letras)
# Saída: ['p', 'y', 't', 'h', 'o', 'n']


# Criando uma lista com números usando range()
# range(10) gera números de 0 a 9
# list() converte esse intervalo em uma lista
numeros = list(range(10))
print(numeros)


# Declarando uma lista com diferentes tipos de dados
carro = ["Ferrari", "F8", 4200000, 2020, 2900, "São Paulo", True]
print(carro)
