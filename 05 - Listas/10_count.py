# Método count() em listas


# Cria uma lista com alguns elementos repetidos
cores = ["vermelho", "azul", "verde", "azul"]


# count() retorna o número de ocorrências de um elemento específico na lista.
print(cores.count("vermelho"))
# Saída: 1


print(cores.count("azul"))
# Saída: 2


print(cores.count("verde"))  # 1
# Saída: 1
