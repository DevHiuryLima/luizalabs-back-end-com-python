# Método pop() em listas


# Cria uma lista com algumas linguagens de programação
linguagens = ["python", "js", "c", "java", "csharp"]


# pop() remove e retorna o último elemento da lista
print(linguagens.pop())
# Saída: csharp


print(linguagens.pop())
# Saída: java


print(linguagens.pop())
# Saída: c


print(linguagens.pop(0))
# Saída: python
