# Método reverse() em listas


# Cria uma lista com algumas linguagens de programação
linguagens = ["python", "js", "c", "java", "csharp"]


# reverse() inverte a ordem dos elementos na lista in-place.
# Não retorna uma nova lista.
linguagens.reverse()


print(linguagens)
# Saída: ["csharp", "java", "c", "js", "python"]
