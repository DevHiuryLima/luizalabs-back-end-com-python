# Método index() em listas


# Cria uma lista com algumas linguagens de programação
linguagens = ["python", "js", "c", "java", "csharp"]


# index() retorna o índice da primeira ocorrência de um elemento específico na lista.
# Se o elemento não for encontrado, gera um ValueError.
print(linguagens.index("java"))
# Saída: 3


print(linguagens.index("python"))
# Saída: 0
